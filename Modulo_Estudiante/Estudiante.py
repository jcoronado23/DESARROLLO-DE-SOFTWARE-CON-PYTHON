# Analizar dados de estudiantes

class Estudiante:
    
    def __init__(self, id, nombre, nota_final):
        self.id = id
        self.nombre = nombre
        self.nota_final = nota_final

def mostrarDatos(self):
        print(f"ID: {self.id}, Nombre: {self.nombre}, Nota Final: {self.nota_final}")

